### Basic Assignment
x = 5      # x is 5


### Addition Assignment (+=)

x = 5
x += 3     # x = x + 3 -> x is 8


### Subtraction Assignment (-=)

x = 5
x -= 2     # x = x - 2 -> x is 3


### Multiplication Assignment (*=)

x = 5
x *= 2     # x = x * 2 -> x is 10


### Division Assignment (/=)

x = 5.0
x /= 2     # x = x / 2 -> x is 2.5 (float)


### Floor Division Assignment (//=)

x = 5
x //= 2    # x = x // 2 -> x is 2 (floor)


### Modulus Assignment (%=)

x = 7
x %= 3     # x = x % 3 -> x is 1 (remainder)


### Exponentiation Assignment (**=)

x = 2
x **= 3    # x = x ** 3 -> x is 8


### Notes
# - Compound assignments modify the variable in place (where possible) and are shorthand for opuated assignment.
# - Be careful with `/=` which produces floats; use `//=` for integer floor division.


